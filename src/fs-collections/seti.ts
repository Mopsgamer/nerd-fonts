export function fromParsedPath(parsed: Parsed): NerdIcon {
	const {base} = parsed;

	const foundBase = byBaseSeti?.get(base);
	if (foundBase !== undefined) {
		return foundBase;
	}

	let baseDot = base.indexOf('.');
	if (baseDot < 0) {
		return icons['nf-seti-text'];
	}

	let extension = base.slice(baseDot);
	for (;;) {
		const foundExtension = byExtensionSeti?.get(extension);
		if (foundExtension) {
			return foundExtension;
		}

		baseDot = extension.indexOf('.', 1);
		if (baseDot === -1) {
			break;
		}

		extension = extension.slice(baseDot);
	}

	return icons['nf-seti-text'];
}
